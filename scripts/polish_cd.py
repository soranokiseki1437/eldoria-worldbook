def apply(c, header, yaml_text):
    i = c.find(header)
    if i < 0: return c, False
    ys = c.find('```yaml', i)
    if ys < 0: return c, False
    ye = c.find('```', ys + 7)
    if ye < 0: return c, False
    return c[:ys+7] + '\n' + yaml_text + '\n' + c[ye:], True

