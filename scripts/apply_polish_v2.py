"""Apply polished YAML by finding ### 事件NXX headers and replacing their yaml blocks."""
import re

def apply_by_header(filepath, header_marker, new_yaml):
    """Find event by ### 事件NXX header and replace its yaml block."""
    with open(filepath, 'r', encoding='utf-8') as f:
        content = f.read()

    idx = content.find(header_marker)
    if idx < 0:
        print(f'  NOT FOUND: {header_marker[:60]}')
        return False

    # Find ```yaml after header
    yaml_start = content.find('```yaml', idx)
    if yaml_start < 0:
        print(f'  No yaml: {header_marker[:60]}')
        return False

    yaml_end = content.find('```', yaml_start + 7)
    if yaml_end < 0:
        print(f'  No end: {header_marker[:60]}')
        return False

    inner_start = yaml_start + 7
    inner_end = yaml_end
    new_block = '\n' + new_yaml + '\n'
    content = content[:inner_start] + new_block + content[inner_end:]

    with open(filepath, 'w', encoding='utf-8') as f:
        f.write(content)
    print(f'  OK: {header_marker[:60]}')
    return True

