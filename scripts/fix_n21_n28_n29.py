def replace_event(content, ev_label, new_block):
    """Find event by ### 事件N## header and replace entire block including header"""
    # Find the header
    pattern = r'### 事件' + ev_label + r'[^\n]*\n'
    m = re.search(pattern, content)
    if not m:
        print(f'ERROR: cannot find header for {ev_label}')
        return content, False

    start = m.start()
    # Find next ### 事件N after this one
    rest = content[m.end():]
    next_ev = re.search(r'\n### 事件N\d{2}[^\n]*\n', rest)
    if next_ev:
        end = m.end() + next_ev.start()
    else:
        # Try next section boundary
        next_sec = re.search(r'\n---\n', rest)
        if next_sec:
            end = m.end() + next_sec.start()
        else:
            print(f'ERROR: cannot find end boundary for {ev_label}')
            return content, False

    old_block = content[start:end]
    result = content[:start] + new_block + content[end:]
    return result, True

