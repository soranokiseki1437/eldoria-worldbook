"""Safely apply polished YAML to 05_事件系统.md by replacing first occurrence only."""
import re

def apply_polish(filepath, event_marker, new_yaml):
    """Find event by YAML title marker and replace its entire ```yaml block."""
    with open(filepath, 'r', encoding='utf-8') as f:
        content = f.read()

    # Find the event header
    idx = content.find(event_marker)
    if idx < 0:
        print(f'  NOT FOUND: {event_marker[:60]}')
        return False

    # Find the ```yaml start
    yaml_start = content.find('```yaml', idx)
    if yaml_start < 0:
        print(f'  No yaml block for: {event_marker[:60]}')
        return False

    # Find the closing ```
    yaml_end = content.find('```', yaml_start + 7)
    if yaml_end < 0:
        print(f'  No closing ``` for: {event_marker[:60]}')
        return False

    # Replace the yaml content (between ```yaml\n and \n```)
    inner_start = yaml_start + 7  # after ```yaml
    inner_end = yaml_end

    old_inner = content[inner_start:inner_end]
    new_block = '\n' + new_yaml + '\n'

    content = content[:inner_start] + new_block + content[inner_end:]

    with open(filepath, 'w', encoding='utf-8') as f:
        f.write(content)

    print(f'  OK: {event_marker[:60]}')
    return True

