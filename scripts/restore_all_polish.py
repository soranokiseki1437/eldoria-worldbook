def apply(content, header, yaml_text):
    i = content.find(header)
    if i < 0: return content, False
    ys = content.find('```yaml', i)
    if ys < 0: return content, False
    ye = content.find('```', ys + 7)
    if ye < 0: return content, False
    return content[:ys+7] + '\n' + yaml_text + '\n' + content[ye:], True

