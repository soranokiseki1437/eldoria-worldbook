def append_content(build_text, uid, addition):
    """在entry的content末尾追加文本（在最后一个content行追加）"""
    pattern = rf'(# === uid {uid}:.*?===\s*\n\s*entries\.append\(make_entry\(\s*\n\s*uid={uid},.*?content=\(\s*\n)(.*?)(\n\s*\),)'
    match = re.search(pattern, build_text, re.DOTALL)
    if not match:
        return build_text, False

    content_section = match.group(2)
    # Find the last content line (before the closing ),)
    # The content ends with a string like '"last line\n"'
    last_line_match = re.search(r'("[^"]*\\n")\s*$', content_section)
    if not last_line_match:
        return build_text, False

    # Insert the addition as a new line before the last line
    last_line = last_line_match.group(1)
    addition_escaped_lines = []
    for line in addition.split('\n'):
        if line:
            escaped = line.replace('\\', '\\\\').replace('"', '\\"')
            addition_escaped_lines.append(f'            "{escaped}\\n"')

    # Build new content: keep everything before last line, add new lines, then last line
    before_last = content_section[:last_line_match.start()]
    new_content = before_last + '\n'.join(addition_escaped_lines) + '\n' + last_line

    old = match.group(0)
    new = match.group(1) + new_content + match.group(3)
    return build_text.replace(old, new, 1), True

