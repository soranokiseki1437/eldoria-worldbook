def build_content_str(text):
    """将纯文本转换为 build_eldoria.py content=() 格式"""
    lines = text.split('\n')
    result_lines = []
    for line in lines:
        escaped = line.replace('\\', '\\\\').replace('"', '\\"')
        if line == lines[-1] and line == '':
            break
        result_lines.append(f'            "{escaped}\\n"')
    return '\n'.join(result_lines)

def find_and_replace_entry(build_text, uid, new_text):
    """替换 content=(...) 块"""
    pattern = rf'(# === uid {uid}:.*?===\s*\n\s*entries\.append\(make_entry\(\s*\n\s*uid={uid},.*?content=\(\s*\n)(.*?)(\n\s*\),)'
    match = re.search(pattern, build_text, re.DOTALL)
    if not match:
        return build_text, False
    new_content_str = build_content_str(new_text)
    old = match.group(0)
    new = match.group(1) + new_content_str + match.group(3)
    return build_text.replace(old, new, 1), True

