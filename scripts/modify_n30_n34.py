# Helper: find event YAML block by header
# ============================================================
def find_event_block(text, event_id):
    """找到事件### 事件N##：标题 的完整YAML块（含header和```yaml...```），返回(start, end)"""
    pattern = rf'(### 事件{event_id}[：:][^\n]*\n\n```yaml\n.*?\n```)'
    m = re.search(pattern, text, re.DOTALL)
    if m:
        return m.start(), m.end(), m.group(1)
    raise ValueError(f'Event {event_id} not found')

def replace_event_block(text, event_id, new_block):
    """替换整个事件块"""
    start, end, old = find_event_block(text, event_id)
    return text[:start] + new_block + text[end:]

